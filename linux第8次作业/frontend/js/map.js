// ===== Tile Rendering Colors =====
const COLORS = {
    BRICK: '#b5651d',
    BRICK_LIGHT: '#c97a35',
    STEEL: '#888888',
    STEEL_LIGHT: '#aaaaaa',
    WATER: '#1a5276',
    WATER_LIGHT: '#2e86c1',
    GRASS: '#2d6a2d',
    GRASS_LIGHT: '#3e8e3e',
    GROUND: '#2a2a2a'
};

// ===== Map Class =====
class GameMap {
    constructor(levelConfig) {
        this.levelConfig = levelConfig;
        this.grid = levelConfig.layout.map(row => [...row]);
        this.baseAlive = true;
        BASE.destroyed = false;
    }

    reset() {
        this.grid = this.levelConfig.layout.map(row => [...row]);
        this.baseAlive = true;
        BASE.destroyed = false;
    }

    getTile(col, row) {
        if (col < 0 || col >= COLS || row < 0 || row >= ROWS) return null;
        return this.grid[row][col];
    }

    setTile(col, row, value) {
        if (col < 0 || col >= COLS || row < 0 || row >= ROWS) return;
        this.grid[row][col] = value;
    }

    // Check if a rectangular area is passable (no walls/water)
    isPassable(x, y, width, height) {
        const left = Math.floor(x / TILE_SIZE);
        const top = Math.floor(y / TILE_SIZE);
        const right = Math.floor((x + width - 1) / TILE_SIZE);
        const bottom = Math.floor((y + height - 1) / TILE_SIZE);

        for (let r = top; r <= bottom; r++) {
            for (let c = left; c <= right; c++) {
                const tile = this.getTile(c, r);
                if (tile === null) return false;
                if (tile === TILE.BRICK || tile === TILE.STEEL || tile === TILE.WATER) {
                    return false;
                }
            }
        }
        return true;
    }

    // Check bullet collision with walls
    handleBulletCollision(bullet) {
        const cx = Math.floor((bullet.x + bullet.size / 2) / TILE_SIZE);
        const cy = Math.floor((bullet.y + bullet.size / 2) / TILE_SIZE);
        const tile = this.getTile(cx, cy);

        if (tile === TILE.BRICK) {
            this.setTile(cx, cy, TILE.EMPTY);
            return true;
        }
        if (tile === TILE.STEEL) {
            return true;
        }
        return false;
    }

    // Check if bullet hits the base
    checkBaseHit(bullet) {
        if (this.baseAlive &&
            bullet.x + bullet.size > BASE.x &&
            bullet.x < BASE.x + BASE.width &&
            bullet.y + bullet.size > BASE.y &&
            bullet.y < BASE.y + BASE.height) {
            this.baseAlive = false;
            BASE.destroyed = true;
            return true;
        }
        return false;
    }

    // Draw the map
    draw(ctx) {
        for (let row = 0; row < ROWS; row++) {
            for (let col = 0; col < COLS; col++) {
                const tile = this.grid[row][col];
                const x = col * TILE_SIZE;
                const y = row * TILE_SIZE;

                switch (tile) {
                    case TILE.BRICK:
                        this.drawBrick(ctx, x, y);
                        break;
                    case TILE.STEEL:
                        this.drawSteel(ctx, x, y);
                        break;
                    case TILE.WATER:
                        this.drawWater(ctx, x, y);
                        break;
                    case TILE.GRASS:
                        this.drawGrass(ctx, x, y);
                        break;
                }
            }
        }

        if (this.baseAlive) {
            this.drawBase(ctx);
        }
    }

    drawBrick(ctx, x, y) {
        ctx.fillStyle = COLORS.BRICK;
        ctx.fillRect(x, y, TILE_SIZE, TILE_SIZE);
        ctx.strokeStyle = COLORS.BRICK_LIGHT;
        ctx.lineWidth = 1;
        ctx.strokeRect(x, y, TILE_SIZE, TILE_SIZE);
        const half = TILE_SIZE / 2;
        ctx.beginPath();
        ctx.moveTo(x, y + half);
        ctx.lineTo(x + TILE_SIZE, y + half);
        ctx.moveTo(x + half, y);
        ctx.lineTo(x + half, y + half);
        ctx.moveTo(x, y + half);
        ctx.lineTo(x, y + TILE_SIZE);
        ctx.moveTo(x + half, y + half);
        ctx.lineTo(x + half, y + TILE_SIZE);
        ctx.stroke();
    }

    drawSteel(ctx, x, y) {
        ctx.fillStyle = COLORS.STEEL;
        ctx.fillRect(x, y, TILE_SIZE, TILE_SIZE);
        ctx.strokeStyle = COLORS.STEEL_LIGHT;
        ctx.lineWidth = 1;
        ctx.strokeRect(x, y, TILE_SIZE, TILE_SIZE);
        ctx.fillStyle = '#999';
        [[8,8],[24,8],[8,24],[24,24]].forEach(([ox, oy]) => {
            ctx.beginPath();
            ctx.arc(x + ox, y + oy, 3, 0, Math.PI * 2);
            ctx.fill();
        });
    }

    drawWater(ctx, x, y) {
        ctx.fillStyle = COLORS.WATER;
        ctx.fillRect(x, y, TILE_SIZE, TILE_SIZE);
        ctx.strokeStyle = COLORS.WATER_LIGHT;
        ctx.lineWidth = 1;
        for (let i = 0; i < 3; i++) {
            ctx.beginPath();
            ctx.moveTo(x, y + 8 + i * 10);
            ctx.quadraticCurveTo(x + 8, y + 4 + i * 10, x + 16, y + 8 + i * 10);
            ctx.quadraticCurveTo(x + 24, y + 12 + i * 10, x + 32, y + 8 + i * 10);
            ctx.stroke();
        }
    }

    drawGrass(ctx, x, y) {
        ctx.fillStyle = COLORS.GROUND;
        ctx.fillRect(x, y, TILE_SIZE, TILE_SIZE);
        ctx.fillStyle = COLORS.GRASS;
        for (let i = 0; i < 5; i++) {
            const gx = x + 4 + Math.random() * 24;
            const gy = y + 4 + Math.random() * 24;
            ctx.fillRect(gx, gy, 4, 6);
        }
    }

    drawBase(ctx) {
        const x = BASE.x, y = BASE.y, w = BASE.width, h = BASE.height;
        ctx.fillStyle = '#555';
        ctx.fillRect(x, y + h - 8, w, 8);
        ctx.strokeStyle = '#ddd';
        ctx.lineWidth = 3;
        ctx.beginPath();
        ctx.moveTo(x + 10, y + h - 8);
        ctx.lineTo(x + 10, y + 4);
        ctx.stroke();
        const grad = ctx.createLinearGradient(x + 10, y + 4, x + w - 6, y + h / 3 + 10);
        grad.addColorStop(0, '#e74c3c');
        grad.addColorStop(1, '#c0392b');
        ctx.fillStyle = grad;
        ctx.beginPath();
        ctx.moveTo(x + 10, y + 4);
        ctx.lineTo(x + w - 6, y + h / 3);
        ctx.lineTo(x + 10, y + h / 3 + 10);
        ctx.closePath();
        ctx.fill();
        ctx.fillStyle = '#ffd700';
        ctx.font = '18px sans-serif';
        ctx.textAlign = 'center';
        ctx.fillText('★', x + w / 2 - 4, y + h / 3 + 8);
    }
}
