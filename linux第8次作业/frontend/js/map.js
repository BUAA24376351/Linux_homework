// Parse a string map into a 2D number array
// . = EMPTY, B = BRICK, S = STEEL, W = WATER, G = GRASS
function parseMap(str) {
    const lines = str.trim().split('\n');
    return lines.map(line => {
        const row = [];
        for (const ch of line) {
            switch (ch) {
                case 'B': row.push(TILE.BRICK); break;
                case 'S': row.push(TILE.STEEL); break;
                case 'W': row.push(TILE.WATER); break;
                case 'G': row.push(TILE.GRASS); break;
                default:  row.push(TILE.EMPTY); break;
            }
        }
        return row;
    });
}

// Auto-generate base protection walls around the base area (cols 13-16, rows 16-19)
// This is called for every level to ensure base is never directly exposed
function protectBase(layout) {
    // Base occupies: rows 18-19, cols 13-16
    // Surround it with brick walls in a U-shape (open at bottom, but bottom is edge)
    const walls = [
        // Top edge of base area
        [16, 12], [16, 13], [16, 14], [16, 15], [16, 16], [16, 17],
        // Left edge
        [17, 12], [18, 12], [19, 12],
        // Right edge
        [17, 17], [18, 17], [19, 17],
        // Bottom edge (base flag tiles themselves)
        [18, 13], [18, 14], [18, 15], [18, 16],
        [19, 13], [19, 14], [19, 15], [19, 16],
    ];
    for (const [r, c] of walls) {
        if (r >= 0 && r < ROWS && c >= 0 && c < COLS) {
            if (layout[r][c] === TILE.EMPTY) {
                layout[r][c] = TILE.BRICK;
            }
        }
    }
    // Clear the 4 tiles where the base flag sits (center)
    layout[18][14] = TILE.EMPTY;
    layout[18][15] = TILE.EMPTY;
    layout[19][14] = TILE.EMPTY;
    layout[19][15] = TILE.EMPTY;
    return layout;
}

